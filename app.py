import streamlit as st
import pandas as pd
import joblib
import numpy as np

# Configure Streamlit
st.set_page_config(page_title="House Price Prediction", layout="centered")

# Load model and features
model = joblib.load("ames_final_model.pkl")
feature_columns = joblib.load("feature_columns.pkl")

st.title("🏠 House Price Prediction App")
st.write("Enter values for each feature to predict house prices")

input_data = {}

# Create input fields with number inputs instead of text
for col in feature_columns:
    input_data[col] = st.number_input(col, value=0.0, format="%.2f")

if st.button("Predict", type="primary"):
    try:
        df = pd.DataFrame([input_data])
        prediction = model.predict(df)
        price = np.exp(prediction)[0]
        st.success(f"🎯 Predicted Price: ${price:,.2f}")
    except Exception as e:
        st.error(f"Error making prediction: {str(e)}")
